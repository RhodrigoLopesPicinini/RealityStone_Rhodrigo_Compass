class Calculadora

    def somar(v1, v2)
        resultado = v1 + v2
        resultado
    end

    def subtrair(v1, v2)
        resultado = v1 - v2
        resultado
    end

    def dividir(v1, v2)
        resultado = v1 / v2
        resultado
    end

    def multiplicar(v1, v2)
        resultado = v1 * v2
        resultado
    end

end